    private void loadData(){
        //creating bokject for access to the BookRankingSystem's method
        BookRankingSystem bookRankingSystem = new BookRankingSystem();
        
        ArrayList<Book> sortedBooks = bookRankingSystem.loadBookRankings();
        
        for (Book book : sortedBooks){
            //access get methods in Book class
            String title = book.getTitle();
            String author = book.getAuthor();
            int rank = book.getRank();
            //add data to table
            Object[] row = {title, author, rank};
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(row);
        }
    }

