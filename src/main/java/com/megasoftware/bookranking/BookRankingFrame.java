    private void btnSearchBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchBookActionPerformed
        
        BookRankingSystem bookRankingSystem = new BookRankingSystem();
        String titleSearch = txtSearch.getText();//storing input value
        Book foundBook = bookRankingSystem.searchBook(titleSearch);
        if (foundBook != null){
            String title = foundBook.getTitle();
            String author = foundBook.getAuthor();
            int rank = foundBook.getRank();
            JOptionPane.showMessageDialog(rootPane, "Title: "+title+" , "+"Author: "+author+" , "+"Rank: "+rank);
            txtSearch.setText("");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Book not found");
            txtSearch.setText("");
        }
    }//GEN-LAST:event_btnSearchBookActionPerformed

